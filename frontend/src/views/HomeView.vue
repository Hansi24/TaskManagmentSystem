<script>

  import axios from 'axios'
  export default {
    name: 'HomeView',
    data(){
      return {
        tasks: null
      }
    },
    methods: {
      fetchTasks(){
        axios.get('http://localhost:5000/tasks/allTask').then((res) => {
            this.tasks = res.data.tasks
        }).catch((err) => {
            console.log(err)
        })
      },
      deleteTask(id){
        axios.delete(`http://localhost:5000/tasks/deleteTask/${id}`).then((res) => {
          if(res.data.status == 'Deleted'){
            this.tasks = res.data.tasks
          }
        }).catch((err) => {
            console.log(err)
        })
      },
      filterByStatus(status){
        axios.get(`http://localhost:5000/tasks/filterTask/${status}`).then((res) => {
            this.tasks = res.data.tasks
        }).catch((err) => {
            console.log(err)
        })
      }
    },
    mounted(){
      this.fetchTasks()
    }
  }
</script>

